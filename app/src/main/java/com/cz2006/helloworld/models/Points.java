package com.cz2006.helloworld.models;

/**
 * Represents Points accumulated by the User
 *
 * @author Rosario Gelli Ann
 *
 */
public class Points {
    int pointsID;
    int points;

    public int getPointsID() {
        return pointsID;
    }

    public void setPointsID(int pointsID) {
        this.pointsID = pointsID;
    }

    public int getPoints() {
        return points;
    }

    public void setPoints(int points) {
        this.points = points;
    }
}
